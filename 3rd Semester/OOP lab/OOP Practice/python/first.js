// Function to add two numbers
function addNumbers(num1, num2) {
    return num1 + num2;
  }
  
  // Example usage
  const firstNumber = 5;
  const secondNumber = 10;
  
  const sum = addNumbers(firstNumber, secondNumber);
  
  console.log(`The sum of ${firstNumber} and ${secondNumber} is: ${sum}`);
  